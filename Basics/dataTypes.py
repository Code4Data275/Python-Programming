name = "Aldous" # String: str
print(name,type(name))

age = 21 # Integer: int
print(age,type(age))

weight = 70.5 # Float: float
print(weight,type(weight))

x = 1j # Complex: complex
print(x,type(x))

players = ["Virat","Rohit","Jasprit","Suryakumar"] # List
print(players,type(players))

companies = ("Google","Microsoft","Amazon","Meta","Netflix","Delloite","Accenture") # Tuples
print(companies,type(companies))

x = range(10) # range
print(x,type(x))

x = True # Boolean: bool
print(x,type(x))

x = b"Hello" # Bytes: bytes
print(x,type(x))

x = bytearray(5) # bytearray
print(x,type(x))

x = memoryview(bytes(5))
print(x,type(x))

x = None
print(x,type(x))
