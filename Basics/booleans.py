# Boolean values
print(10 > 9)
print(10 == 9)
print(10 < 9)

# If statement
a = 200
b = 33

if b > a:
    print("b is greater than a")
else:
    print("b is not greater than a")

# bool() function
x = "Aldous"
y = 21
print(bool(x))
print(bool(y))

# True Value
print(bool('abc'))
print(bool(123))
print(bool(["apple","cherry","banana"]))

# False value
print(bool(False))
print(bool(None))
print(bool(0))
print(bool(""))
print(bool(()))
print(bool([]))
print(bool({}))


